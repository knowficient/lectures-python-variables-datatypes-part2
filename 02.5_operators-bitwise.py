# Bitwise AND &
print("0b0010=",0b0010)
print("0b0011=",0b0011)
print("Bitwise AND = ",0b0010 & 0b0011)

# Bitwise OR |
print("0b0010=",0b0010)
print("0b0011=",0b0011)
print("Bitwise OR = ",0b0010 | 0b0011)

# Bitwise XOR ^
print("0b0010=",0b0010)
print("0b0011=",0b0011)
print("Bitwise XOR = ",0b0010 ^ 0b0011)

# Bitwise One's Complement ~
print("0b00000010=",0b00000010)
print("One's Complement = ",~0b00000010)

# Bitwise Left-Shift <<
print("0b0011=",0b0011)
print("Left Shift << 1 = ",0b0011<<1)

# Bitwise Right-Shift >>
print("0b0011=",0b0011)
print("Right Shift >> 1 = ",0b0011>>1)