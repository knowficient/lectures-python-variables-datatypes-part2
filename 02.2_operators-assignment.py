# Multiply & Assign *=
a*=3
print("Multiply & Assign a*=3 ->", a)

# Divide & Assign /=
a/=3
print("Multiply & Assign a/=3 ->", a)

# Floor Division & Assign //=
a//=3
print("Floor Dvision & Assign a//=3 ->", a)

# Modulus & Assign %=
a%3
print("Modulus & Assign a%=3 ->", a)

# Exponent & Assign **=
a**=3
